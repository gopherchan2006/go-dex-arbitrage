// Package contract provides Go bindings and types for the ArbitrageExecutor
// Solidity smart contract.
//
// # What is a smart contract?
//
// A smart contract is a program deployed on the Ethereum blockchain.
// Once deployed, it lives at an address and can be called by anyone.
// Its code is immutable and executes deterministically.
//
// # Why do we need a contract for arbitrage?
//
// Arbitrage involves multiple transactions: buy on DEX A, sell on DEX B.
// If we send two separate transactions:
//   - Tx1: buy on Uniswap
//   - Tx2: sell on SushiSwap
//
// There's a race condition: another bot could step in between Tx1 and Tx2,
// making Tx2 unprofitable. Worse, if Tx2 fails, we've already spent money on Tx1.
//
// The solution: bundle both swaps into ONE transaction via a smart contract.
// The contract is atomic: either both swaps succeed, or neither executes.
// This is the core pattern for on-chain arbitrage.
//
// # Flash Loans
//
// A flash loan allows you to borrow an arbitrary amount of tokens, execute
// arbitrary logic (like arbitrage), and repay the loan – all in one transaction.
// If you don't repay, the entire transaction reverts. Zero counterparty risk.
//
// This means capital requirements are effectively zero for the borrower:
// you borrow $1M, run the arb, keep the profit, repay $1M + fee.
// AAVE charges 0.09% for flash loans.
//
// # ABI and Go bindings
//
// To call a Solidity function from Go:
//  1. Compile the Solidity to ABI (Application Binary Interface)
//  2. Use `abigen` to generate Go structs and call helpers from the ABI
//  3. In your Go code: call `contract.ExecuteArbitrage(auth, params...)`
//
// The bindings generated here are hand-written representations of what
// `abigen` would produce. In a real project, run:
//
//	solc --abi contracts/ArbitrageExecutor.sol -o build/
//	abigen --abi build/ArbitrageExecutor.abi --pkg contract --out internal/contract/bindings.go
